//manipulador de base numericas
//conversão de um tipo para outro
#include <iostream>
using namespace std;
int main()
{
    int n=65;
    cout <<'\n'<<"Hexadecimal: "<< hex << n; //converte de inteiro para hexadecimal
    cout <<'\n'<<"Octal      : "<< oct << n; //converte de inteiro para octal
    cout <<'\n'<<"Decimal    : "<< dec << n; //converte de inteiro para decimal
    cout << endl;
    cin.get();
}
